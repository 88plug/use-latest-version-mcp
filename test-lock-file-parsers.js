#!/usr/bin/env node

/**
 * Test script for lock file parsers
 */

import { parseLockFile, getLockParserForFile, LOCK_PARSERS, PnpmLockParser, NpmLockParser } from './build/lock-file-parsers.js';
import { writeFileSync, mkdirSync, existsSync } from 'fs';
import { join } from 'path';

console.log('=== Lock File Parser Tests ===\n');

// Test counters
let passed = 0;
let failed = 0;

function test(name, fn) {
  try {
    fn();
    console.log(`✅ ${name}`);
    passed++;
  } catch (error) {
    console.log(`❌ ${name}`);
    console.log(`   Error: ${error.message}`);
    failed++;
  }
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message || 'Assertion failed');
  }
}

// Create test directory
const testDir = './test-dependency-files';
if (!existsSync(testDir)) {
  mkdirSync(testDir, { recursive: true });
}

// ============================================================================
// Test 1: Parser Registry
// ============================================================================
console.log('\n--- Parser Registry ---');

test('LOCK_PARSERS has all expected parsers', () => {
  assert(LOCK_PARSERS.length === 8, 'should have 8 parsers');
});

test('getLockParserForFile finds npm parser', () => {
  const parser = getLockParserForFile('package-lock.json');
  assert(parser !== null, 'should find parser for package-lock.json');
  assert(parser?.name === 'npm', 'should be npm parser');
});

test('getLockParserForFile finds yarn parser', () => {
  const parser = getLockParserForFile('yarn.lock');
  assert(parser !== null, 'should find parser for yarn.lock');
  assert(parser?.name === 'yarn', 'should be yarn parser');
});

test('getLockParserForFile finds go parser', () => {
  const parser = getLockParserForFile('go.sum');
  assert(parser !== null, 'should find parser for go.sum');
  assert(parser?.name === 'go', 'should be go parser');
});

test('getLockParserForFile returns null for unknown file', () => {
  const parser = getLockParserForFile('unknown.lock');
  assert(parser === null, 'should return null for unknown file');
});

// ============================================================================
// Test 2: npm Lock Parser (package-lock.json v3)
// ============================================================================
console.log('\n--- npm Lock Parser ---');

const packageLockJson = {
  name: 'test-project',
  version: '1.0.0',
  lockfileVersion: 3,
  packages: {
    '': { name: 'test-project', version: '1.0.0' },
    'node_modules/express': {
      version: '4.18.2',
      resolved: 'https://registry.npmjs.org/express/-/express-4.18.2.tgz',
      integrity: 'sha512-xxx',
    },
    'node_modules/lodash': {
      version: '4.17.21',
      resolved: 'https://registry.npmjs.org/lodash/-/lodash-4.17.21.tgz',
      integrity: 'sha512-yyy',
    },
  },
};

writeFileSync(join(testDir, 'package-lock.json'), JSON.stringify(packageLockJson, null, 2));

test('npm lock parser parses package-lock.json', () => {
  const result = parseLockFile(join(testDir, 'package-lock.json'));
  assert(result.errors.length === 0, 'should have no errors');
  assert(result.dependencies.length === 2, 'should have 2 dependencies');
});

test('npm lock parser extracts versions', () => {
  const result = parseLockFile(join(testDir, 'package-lock.json'));
  const express = result.dependencies.find(d => d.name === 'express');
  assert(express?.version === '4.18.2', 'should extract version 4.18.2');
});

test('npm lock parser extracts resolved URLs', () => {
  const result = parseLockFile(join(testDir, 'package-lock.json'));
  const express = result.dependencies.find(d => d.name === 'express');
  assert(express?.resolved?.includes('registry.npmjs.org'), 'should extract resolved URL');
});

test('npm lock parser extracts integrity hashes', () => {
  const result = parseLockFile(join(testDir, 'package-lock.json'));
  const express = result.dependencies.find(d => d.name === 'express');
  assert(express?.integrity !== undefined, 'should extract integrity hash');
});

